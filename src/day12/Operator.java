package day12;

public class Operator {
    public static void main(String[] args) {
        //声名变量
        int i1=7;
        int i2=2;
        System.out.println("i1="+i1+"\ni2="+i2);
        System.out.println("i1+i2="+(i1+i2));
        System.out.println("i1-i2="+(i1-i2));
        System.out.println("i1*i2="+(i1*i2));
        System.out.println("i1/i2="+(i1/(double)i2));

        System.out.println("---------------------");

        //声名double变量
        double d1=8.0;
        double d2=2.5;
        System.out.println("d1="+d1+"\nd2="+d2);


    }
}
