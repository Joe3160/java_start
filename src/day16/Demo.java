package day16;

/**
 * 流程控制之 循环结构
 */
public class Demo {
    public static void main(String[] args){
        //while循环
        int i=0;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        System.out.println("---------------");
        //do……while循环
        int j=20;
        do {
            System.out.println(j);
            j++;
        } while (j<=10);


    }
}
