package day13;

/***
 * 赋值运算符
 */
public class Demo {
    public static  void main(String[] args){
        int i=1;
        i+=5;
        System.out.println(i);//6
        i-=2;
        System.out.println(i);//4
        i*=2;
        System.out.println(i);//8
        i/=3;
        System.out.println(i);//2
        i%=2;
        System.out.println(i);//0


    }
}
